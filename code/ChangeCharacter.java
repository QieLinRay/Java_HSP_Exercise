public class ChangeCharacter{
	public static void main(String[] args){
		System.out.println("BEIJING \n SHANGHAI \n TIANJINS");
		System.out.println("上海大学\r财经");
		//输出的是财经大学，回车是光标回到开头
		System.out.println("上海大学\r\n财经");
		System.out.println("上海\t北京\t天津");
		System.out.println("悟已往之不谏\n知来者之可追");
		System.out.println("老师说：\"硕博连读吧\"");
		System.out.println("\\\\");
		System.out.println("书名\t作者\t价格\t销量\n三国\t罗贯中\t120\t1000");

	}
}